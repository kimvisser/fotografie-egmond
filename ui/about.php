            <h3>Wie zijn wij?</h3>
            <img src="img/olofkim.png" align="right" class="border" width="180">
            <p>Kim Visser (1977) en Olof van der Steen (1978) zijn beiden opgegroeid en nog steeds woonachtig in de Egmonden. 
            Sinds hun vroege jeugd zijn zij goede vrienden en enkele jaren geleden begonnen zij ook samen met fotograferen. 
            Wat begon als een hobby is inmiddels uitgegroeid tot een professionele bezigheid. In de loop der jaren hebben zij 
            zichzelf nieuwe fotografische technieken aangeleerd en veel kennis werd opgedaan aan de Fotovakschool te Amsterdam.</p>
            <p>Natuur- en landschapsfotografie is hun grootste specialiteit. Portret- en product fotografie of bijvoorbeeld bedrijfsreportages 
            behoren ook tot hun werkgebied. Op Fotografie Egmond kunt u kennismaken met het werk van deze fotografen.</p>
            <p>Op Fotografie-Egmond.nl kunt u <a href="portfolio">hun mooiste werk</a> bestellen voor thuis of in uw bedrijf aan de muur!</p>
            <p>Heeft u speciale wensen? Neem dan <a href="contact">contact</a> met hen op.</p>
