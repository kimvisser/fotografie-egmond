            <h4>Kwaliteitsverschil</h4>
            
            <p>Er is een flink verschil tussen Acryluxe en bestaande producten zoals foto op aluminium en foto op plexiglas. Bij Acryluxe wordt een echte fotoafdruk tussen het aluminium en de acrylaatglasplaat gelamineerd, terwijl bij goedkopere fotoproducten de afbeelding met een printer op het materiaal wordt gedrukt. Het kwaliteitsverschil in scherpte en kleur is aanzienlijk. Bij Acryluxe komen de details en kleuren maximaal tot hun recht. Een foto in museumkwaliteit aan de muur!</p>
            <p>Wist u dat acryl en plexiglas hetzelfde is? Plexiglas is echter een productnaam, acrylaat de materiaalsoort. Acrylaat en plexi worden vaak glas genoemd, dat is niet helemaal juist. Acrylaat is een gehard kunststof dat meer licht doorlaat dan glas en daardoor beter geschikt is voor het veredelen van foto’s.</p>

